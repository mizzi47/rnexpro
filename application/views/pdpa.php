                               <h3> <p>Personal Data Protection Policy for Rnexpro.com</p></h3> 
                               

                               <p><b> 1. Introduction</b> 
                                 <br>At Rnexpro.com , we are committed to safeguarding the privacy of our users and ensuring 
                                    that your personal data is protected. This policy outlines our practices regarding the 
                                   collection, use, and disclosure of personal data in accordance with the Personal Data 
                                   Protection Act (PDPA) 2010 of Malaysia.<br></p>

                               <p> <b>2. Scope and Applicability</b>
                                   <br>This policy applies to all visitors and users of Rnexpro.com . It covers all personal data 
                                   collected through our website and associated services.<br></p>
                                   
                                <p><b> 3. Key Definitions</b>

                                   <br>Personal Data: Any information that relates directly or indirectly to an individual who 
                                   can be identified from that information.<br>
                                   <br>Sensitive Personal Data: Information regarding an individual's health, political 
                                   opinions, religious beliefs, etc., requiring stricter handling.<br> 

                                <p><b>4. Data Protection Principles Rnexpro.com adheres to the seven PDPA principles as follows:</b>
                                
                                <p> 1. General Principle
                                   <br> Personal data will only be processed with the consent of the data subject or when necessary 
                                    for contractual, legal, or specified purposes.</br></p>

<p>2. Notice and Choice Principle
<br>Users will be informed about the purpose of data collection, and their consent will be obtained 
where necessary. Notices will be clear and understandable.</br></p>

<p>3. Disclosure Principle
<br>Personal data will not be disclosed without the consent of the data subject, except for the 
purposes disclosed at the time of collection or as permitted by law.</br></p>

<p>4. Security Principle
<br>Appropriate technical and organizational measures will be taken to protect personal data from 
loss, misuse, unauthorized access, modification, or disclosure.</br></p>

<p>5. Retention Principle
<br>Personal data will not be kept longer than necessary for the fulfillment of the purpose it was 
collected for. Retention schedules and disposal methods will be established.</br></p>

<p>6. Data Integrity Principle
<br>Personal data will be accurate, complete, and kept up-to-date. Measures will be taken to 
ensure the accuracy of data collected.</br></p>

<p>7. Access Principle
<br>Users will have the right to access their personal data and correct any inaccuracies. 
Procedures will be in place to handle such requests efficiently.</br></p>


<p><b>5. Collection of Personal Data</b>
<br> We may collect personal data from you when you:
<br>1. Register or create an account on our website.
<br>2. Subscribe to our newsletter.
<br>3. Fill out a contact form or request information.
<br>4. Participate in surveys or promotional activities.</br>
 <br>The types of personal data we collect may include, but are not limited to:</br>
<br>Name
<br>Email address
<br>Phone number
<br>Mailing address
<br>Payment information (for transactions)
<br>IP address <br></p>

<p> <b>6. Use of Personal Data</b>
<br>The personal data collected may be used for the following purposes:
<br>To provide and manage our services.
<br>To communicate with you regarding your account or transactions.
<br>To send promotional materials or newsletters, with your consent.
<br>To improve our website and services based on user feedback.
<br>To comply with legal obligations.</br></p> 

<p><b>7. Disclosure of Personal Data</b>
<br>We do not sell, trade, or otherwise transfer your personal data to outside parties except as 
described in this policy. Personal data may be disclosed to:
Service providers who assist in operating our website and conducting our business.
Legal and regulatory authorities, if required by law.
Other parties with your explicit consent.</br></p> 

<p> <b>8. Security Measures</b>
<br>We implement various security measures to ensure the protection of your personal data, 
including:</br>
<br> Regular security audits and updates.<br>
<br>Access controls to limit data access to authorized personnel only.</br></p>

<p><b>9. Retention and Disposal</b>
<br>Personal data will be retained only as long as necessary for the purposes stated. Once the 
data is no longer needed, it will be securely deleted or anonymized.<br></p>

<p> <b>10. Rights of Data Subjects</b>
<br>Users have the following rights regarding their personal data:</br>
<br>Right to Access: Request access to their personal data held by us.</br>
<br> Right to Correct: Request corrections to their personal data to ensure accuracy and 
completeness.</br>
<br>Right to Withdraw Consent: Withdraw consent for the processing of their personal data 
at any time.</br></p>

<p><b> 11. Cookies and Tracking Technologies</b>
<br>Our website may use cookies and similar tracking technologies to enhance user experience 
and gather information about website usage. Users can control cookie settings through their 
browser preferences.</br></p> 

<p><b>12. Third-Party Links</b>
<br>Our website may contain links to third-party sites. We are not responsible for the privacy 
practices of these sites and encourage users to read their privacy policies.</br></p>

<p><b> 13. Contact Information</b>
<br>For any inquiries or complaints regarding personal data, please contact our Data Protection 
Officer at:</br>
<br>Email: admin@rnexpro.com</br>
<br>Phone: 03-5525 1885</br></p> 

<p><b>14. Policy Review</b>
<br>This policy will be reviewed regularly and updated as necessary to ensure compliance with 
the PDPA and to reflect any changes in our practices or legal obligations.</br></p> 

<p> By implementing and adhering to this policy, Rnexpro.com demonstrates its commitment to 
protecting personal data in compliance with the PDPA 2010.</p> </h6>
                             

